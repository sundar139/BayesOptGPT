LOGGER = logging.getLogger("bayes_gp_llmops.evaluation.pipeline")
AG_NEWS_CLASS_NAMES = ["World", "Sports", "Business", "Sci/Tech"]


@dataclass(frozen=True)
class SplitOutputs:
    """In-memory predictions and uncertainty tensors for one split."""

    logits: torch.Tensor
    probabilities: torch.Tensor
    labels: torch.Tensor
    predictions: torch.Tensor
    confidence: torch.Tensor
    entropy: torch.Tensor
    margin: torch.Tensor


@dataclass(frozen=True)
class EvaluationArtifacts:
    """Output file paths generated by the evaluation pipeline."""

    output_dir: Path
    metrics_validation_path: Path
    metrics_test_path: Path
    metrics_validation_calibrated_path: Path | None
    metrics_test_calibrated_path: Path | None
    predictions_validation_path: Path
    predictions_test_path: Path
    temperature_scaling_path: Path
    confusion_matrix_plot_path: Path
    reliability_diagram_plot_path: Path
    confidence_histogram_plot_path: Path
    entropy_histogram_plot_path: Path
    metrics_validation: dict[str, object]
    metrics_test: dict[str, object]
    metrics_validation_calibrated: dict[str, object] | None
    metrics_test_calibrated: dict[str, object] | None


def run_evaluation_pipeline(
    *,
    data_config_path: Path,
    model_config_path: Path,
    train_config_path: Path,
    checkpoint_path: Path | None,
    device_override: str | None,
    output_dir: Path,
    enable_temperature_scaling: bool,
    debug_mode: bool,
    data_config_override: DataPipelineConfig | None = None,
    model_config_override: ModelConfig | None = None,
    train_config_override: TrainConfig | None = None,
    mlflow_enabled: bool | None = None,
    mlflow_experiment_name: str | None = None,
    mlflow_run_name: str | None = None,
    mlflow_tags: dict[str, str] | None = None,
    mlflow_nested: bool = False,
) -> EvaluationArtifacts:
    """Run full evaluation with uncertainty analysis and optional calibration."""

    settings = get_settings()
    configure_logging(settings.log_level)

    data_config = data_config_override or load_data_config(data_config_path)
    model_config = model_config_override or load_model_config(model_config_path)
    train_config = train_config_override or load_train_config(train_config_path)
    active_mlflow = settings.enable_mlflow if mlflow_enabled is None else mlflow_enabled
    experiment_name = mlflow_experiment_name or "bayes-gp-llmops-evaluation"

    if device_override is not None:
        train_config = train_config.model_copy(update={"device_preference": device_override})
    if debug_mode:
        debug_subset_size = data_config.dataset.debug_subset_size or 512
        debug_dataset = data_config.dataset.model_copy(
            update={"debug_subset_size": debug_subset_size}
        )
        data_config = data_config.model_copy(update={"dataset": debug_dataset})
        train_config = train_config.model_copy(
            update={
                "max_validation_batches_per_epoch": 10,
                "max_test_batches_per_epoch": 10,
            }
        )

    set_global_seed(train_config.random_seed)
    device = resolve_device(train_config.device_preference)

    if not tokenizer_artifacts_exist(data_config.paths.tokenizer_dir):
        raise FileNotFoundError(
            "Tokenizer artifacts were not found. Run scripts/download_data.py before evaluation."
        )
    tokenizer = load_tokenizer(
        data_config.paths.tokenizer_dir,
        max_sequence_length=data_config.tokenizer.max_sequence_length,
    )
    _validate_tokenizer_compatibility(
        trained_vocab_size=tokenizer.get_vocab_size(),
        configured_vocab_size=model_config.vocab_size,
    )

    resolved_checkpoint_path = checkpoint_path or (train_config.checkpoint_dir / "best.ckpt")
    if not resolved_checkpoint_path.exists():
        raise FileNotFoundError(
            f"Checkpoint not found at {resolved_checkpoint_path}. "
            "Run scripts/train.py to produce artifacts/checkpoints/best.ckpt."
        )

    splits = load_ag_news_splits(data_config)
    split_sizes = summarize_split_sizes(splits)
    LOGGER.info("Loaded dataset splits for evaluation: %s", split_sizes)

    dataloaders = build_split_dataloaders(
        splits,
        tokenizer=tokenizer,
        config=data_config,
        num_classes=model_config.num_classes,
    )
    model = TinyLlamaForSequenceClassification(model_config).to(device)
    load_checkpoint(path=resolved_checkpoint_path, model=model, map_location=device)
    model.eval()

    test_batch_cap = (
        train_config.max_test_batches_per_epoch
        if train_config.max_test_batches_per_epoch is not None
        else train_config.max_validation_batches_per_epoch
    )
    resolved_runtime = {
        "checkpoint_path": str(resolved_checkpoint_path),
        "output_dir": str(output_dir),
        "split_sizes": split_sizes,
        "device": str(device),
        "temperature_scaling_enabled": enable_temperature_scaling,
        "debug_mode": debug_mode,
        "max_validation_batches_per_epoch": train_config.max_validation_batches_per_epoch,
        "max_test_batches_per_epoch": test_batch_cap,
    }
    resolved_params = flatten_mapping(
        {
            "model": model_config.model_dump(mode="json"),
            "training": train_config_to_dict(train_config),
            "runtime": resolved_runtime,
        }
    )

    with start_mlflow_run(
        enabled=active_mlflow,
        experiment_name=experiment_name,
        run_name=mlflow_run_name or "baseline-evaluate",
        nested=mlflow_nested,
        tags=mlflow_tags,
    ):
        log_parameters(resolved_params, enabled=active_mlflow)
        validation_outputs = _run_inference(
            model=model,
            loader=dataloaders.validation,
            device=device,
            max_batches=train_config.max_validation_batches_per_epoch,
        )
        test_outputs = _run_inference(
            model=model,
            loader=dataloaders.test,
            device=device,
            max_batches=test_batch_cap,
        )

        metrics_validation = _compute_metrics(
            outputs=validation_outputs,
            num_classes=model_config.num_classes,
        )
        metrics_test = _compute_metrics(
            outputs=test_outputs,
            num_classes=model_config.num_classes,
        )

        evaluation_output_dir = ensure_directory(output_dir)

        metrics_validation_path = evaluation_output_dir / "metrics_validation.json"
        metrics_test_path = evaluation_output_dir / "metrics_test.json"
        predictions_validation_path = evaluation_output_dir / "predictions_validation.csv"
        predictions_test_path = evaluation_output_dir / "predictions_test.csv"
        temperature_scaling_path = evaluation_output_dir / "temperature_scaling.json"
        confusion_matrix_plot_path = evaluation_output_dir / "confusion_matrix_test.png"
        reliability_diagram_plot_path = evaluation_output_dir / "reliability_diagram_test.png"
        confidence_histogram_plot_path = evaluation_output_dir / "confidence_histogram_test.png"
        entropy_histogram_plot_path = evaluation_output_dir / "entropy_histogram_test.png"

        write_json(metrics_validation_path, metrics_validation)
        write_json(metrics_test_path, metrics_test)
        export_predictions_csv(
            path=predictions_validation_path,
            split="validation",
            labels=validation_outputs.labels,
            predictions=validation_outputs.predictions,
            confidence=validation_outputs.confidence,
            entropy=validation_outputs.entropy,
            margin=validation_outputs.margin,
            logits=validation_outputs.logits,
            probabilities=validation_outputs.probabilities,
        )
        export_predictions_csv(
            path=predictions_test_path,
            split="test",
            labels=test_outputs.labels,
            predictions=test_outputs.predictions,
            confidence=test_outputs.confidence,
            entropy=test_outputs.entropy,
            margin=test_outputs.margin,
            logits=test_outputs.logits,
            probabilities=test_outputs.probabilities,
        )

        confusion_matrix_values = np.asarray(
            cast(list[list[int]], metrics_test["confusion_matrix"]),
            dtype=np.int64,
        )
        plot_confusion_matrix(
            path=confusion_matrix_plot_path,
            confusion_matrix=confusion_matrix_values,
            class_names=AG_NEWS_CLASS_NAMES if model_config.num_classes == 4 else None,
        )

        plot_outputs = test_outputs
        metrics_validation_calibrated_path: Path | None = None
        metrics_test_calibrated_path: Path | None = None
        metrics_validation_calibrated: dict[str, object] | None = None
        metrics_test_calibrated: dict[str, object] | None = None
        temperature_payload: dict[str, object] = {
            "enabled": enable_temperature_scaling,
            "checkpoint_path": str(resolved_checkpoint_path),
        }

        if enable_temperature_scaling:
            scaler = TemperatureScaler(initial_temperature=1.0)
            learned_temperature = scaler.fit(
                logits=validation_outputs.logits,
                labels=validation_outputs.labels,
            )
            validation_calibrated_outputs = _derive_outputs(
                logits=scaler.transform(validation_outputs.logits),
                labels=validation_outputs.labels,
            )
            test_calibrated_outputs = _derive_outputs(
                logits=scaler.transform(test_outputs.logits),
                labels=test_outputs.labels,
            )
            metrics_validation_calibrated = _compute_metrics(
                outputs=validation_calibrated_outputs,
                num_classes=model_config.num_classes,
            )
            metrics_test_calibrated = _compute_metrics(
                outputs=test_calibrated_outputs,
                num_classes=model_config.num_classes,
            )
            metrics_validation_calibrated_path = (
                evaluation_output_dir / "metrics_validation_calibrated.json"
            )
            metrics_test_calibrated_path = (
                evaluation_output_dir / "metrics_test_calibrated.json"
            )
            write_json(metrics_validation_calibrated_path, metrics_validation_calibrated)
            write_json(metrics_test_calibrated_path, metrics_test_calibrated)
            temperature_payload.update(
                {
                    "temperature": learned_temperature,
                    "validation_nll_before": _metric_float(metrics_validation, "nll"),
                    "validation_nll_after": _metric_float(
                        metrics_validation_calibrated,
                        "nll",
                    ),
                    "test_nll_before": _metric_float(metrics_test, "nll"),
                    "test_nll_after": _metric_float(metrics_test_calibrated, "nll"),
                }
            )
            plot_outputs = test_calibrated_outputs

        write_json(temperature_scaling_path, temperature_payload)
        plot_reliability_diagram(
            path=reliability_diagram_plot_path,
            probabilities=plot_outputs.probabilities,
            labels=plot_outputs.labels,
            num_bins=15,
        )
        plot_confidence_histogram(
            path=confidence_histogram_plot_path,
            confidence=plot_outputs.confidence,
        )
        plot_entropy_histogram(path=entropy_histogram_plot_path, entropy=plot_outputs.entropy)

        metric_payload: dict[str, float] = {}
        metric_payload.update(_extract_scalar_metrics(metrics_validation, prefix="validation"))
        metric_payload.update(_extract_scalar_metrics(metrics_test, prefix="test"))
        if metrics_validation_calibrated is not None:
            metric_payload.update(
                _extract_scalar_metrics(
                    metrics_validation_calibrated,
                    prefix="validation_calibrated",
                )
            )
        if metrics_test_calibrated is not None:
            metric_payload.update(
                _extract_scalar_metrics(
                    metrics_test_calibrated,
                    prefix="test_calibrated",
                )
            )
        log_metrics(metric_payload, enabled=active_mlflow)

        artifact_paths = [
            metrics_validation_path,
            metrics_test_path,
            predictions_validation_path,
            predictions_test_path,
            temperature_scaling_path,
            confusion_matrix_plot_path,
            reliability_diagram_plot_path,
            confidence_histogram_plot_path,
            entropy_histogram_plot_path,
        ]
        if metrics_validation_calibrated_path is not None:
            artifact_paths.append(metrics_validation_calibrated_path)
        if metrics_test_calibrated_path is not None:
            artifact_paths.append(metrics_test_calibrated_path)
        log_artifact_files(artifact_paths, enabled=active_mlflow, artifact_path="evaluation")

    LOGGER.info("evaluation_output_dir=%s", evaluation_output_dir)
    return EvaluationArtifacts(
        output_dir=evaluation_output_dir,
        metrics_validation_path=metrics_validation_path,
        metrics_test_path=metrics_test_path,
        metrics_validation_calibrated_path=metrics_validation_calibrated_path,
        metrics_test_calibrated_path=metrics_test_calibrated_path,
        predictions_validation_path=predictions_validation_path,
        predictions_test_path=predictions_test_path,
        temperature_scaling_path=temperature_scaling_path,
        confusion_matrix_plot_path=confusion_matrix_plot_path,
        reliability_diagram_plot_path=reliability_diagram_plot_path,
        confidence_histogram_plot_path=confidence_histogram_plot_path,
        entropy_histogram_plot_path=entropy_histogram_plot_path,
        metrics_validation=metrics_validation,
        metrics_test=metrics_test,
        metrics_validation_calibrated=metrics_validation_calibrated,
        metrics_test_calibrated=metrics_test_calibrated,
    )


def _run_inference(
    *,
    model: TinyLlamaForSequenceClassification,
    loader: DataLoader[dict[str, torch.Tensor]],
    device: torch.device,
    max_batches: int | None,
) -> SplitOutputs:
    logits_chunks: list[torch.Tensor] = []
    labels_chunks: list[torch.Tensor] = []
    processed_batches = 0

    with torch.no_grad():
        for batch in loader:
            processed_batches += 1
            if max_batches is not None and processed_batches > max_batches:
                break
            input_ids = batch["input_ids"].to(device)
            attention_mask = batch["attention_mask"].to(device)
            labels = batch["labels"].to(device)
            logits = model(input_ids, attention_mask=attention_mask)
            logits_chunks.append(logits.detach().cpu())
            labels_chunks.append(labels.detach().cpu())

    if not logits_chunks:
        raise ValueError("No samples were evaluated. Check dataloaders and max batch settings.")
    logits = torch.cat(logits_chunks, dim=0)
    labels = torch.cat(labels_chunks, dim=0)
    return _derive_outputs(logits=logits, labels=labels)


def _derive_outputs(*, logits: torch.Tensor, labels: torch.Tensor) -> SplitOutputs:
    probabilities = probabilities_from_logits(logits)
    predictions = torch.argmax(probabilities, dim=1)
    confidence = max_softmax_confidence(probabilities)
    entropy = predictive_entropy(probabilities)
    margin = confidence_margin(probabilities)
    return SplitOutputs(
        logits=logits,
        probabilities=probabilities,
        labels=labels,
        predictions=predictions,
        confidence=confidence,
        entropy=entropy,
        margin=margin,
    )


def _compute_metrics(*, outputs: SplitOutputs, num_classes: int) -> dict[str, object]:
    labels = outputs.labels.numpy()
    predictions = outputs.predictions.numpy()
    class_labels = list(range(num_classes))

    precision, recall, f1_per_class, _ = precision_recall_fscore_support(
        labels,
        predictions,
        labels=class_labels,
        average=None,
        zero_division=0,
    )
    confusion = confusion_matrix(labels, predictions, labels=class_labels)
    uncertainty_summary = summarize_uncertainty(
        confidence=outputs.confidence,
        entropy=outputs.entropy,
    )

    return {
        "num_samples": int(outputs.labels.size(0)),
        "loss": negative_log_likelihood(outputs.logits, outputs.labels),
        "nll": negative_log_likelihood(outputs.logits, outputs.labels),
        "accuracy": float(accuracy_score(labels, predictions)),
        "macro_f1": float(
            f1_score(labels, predictions, labels=class_labels, average="macro", zero_division=0)
        ),
        "per_class_precision": [float(value) for value in precision.tolist()],
        "per_class_recall": [float(value) for value in recall.tolist()],
        "per_class_f1": [float(value) for value in f1_per_class.tolist()],
        "confusion_matrix": confusion.astype(int).tolist(),
        "brier_score": brier_score(outputs.probabilities, outputs.labels),
        "ece": compute_ece(outputs.probabilities, outputs.labels, num_bins=15),
        "uncertainty_summary": uncertainty_summary,
    }


def _validate_tokenizer_compatibility(
    *,
    trained_vocab_size: int,
    configured_vocab_size: int,
) -> None:
    if configured_vocab_size < trained_vocab_size:
        raise ValueError(
            "Configured model vocab size is smaller than tokenizer vocabulary size: "
            f"{configured_vocab_size} < {trained_vocab_size}"
        )


def _metric_float(metrics: dict[str, object], key: str) -> float:
    value = metrics[key]
    if isinstance(value, float):
        return value
    if isinstance(value, int):
        return float(value)
    raise TypeError(f"Metric '{key}' must be numeric.")


def _extract_scalar_metrics(metrics: dict[str, object], *, prefix: str) -> dict[str, float]:
    scalar_keys = ["loss", "nll", "accuracy", "macro_f1", "brier_score", "ece"]
    payload: dict[str, float] = {}
    for key in scalar_keys:
        value = metrics.get(key)
        if isinstance(value, (int, float)):
            payload[f"{prefix}_{key}"] = float(value)
    return payload
